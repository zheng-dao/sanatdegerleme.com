<script type="text/javascript">
	let slideNumber = 0;
	window.onload = function(){
		const slides = document.getElementsByClassName('panel');
		const slider = document.getElementById("swipe");
		const currentSlide = slider.getElementsByClassName('current');
		const currentSlideback = slider.getElementsByClassName('currentback');
		let prevBtn = document.querySelector("#prev");
		let nextBtn = document.querySelector("#next");
		prevBtn.onclick = function(){
			currentSlide[0].classList.remove("current");
			if(slideNumber == 0){
				slideNumber = slides.length-1;
			}
			else{
				slideNumber = slideNumber-1;
			}
			slides[slideNumber].classList.add("current");
			if (slideNumber == 0) {
				slides[1].classList.add("currentback");
				slides[2].classList.remove("currentback");
				slides[0].classList.remove("currentback");
			}
			else if(slideNumber == 1){
				slides[2].classList.add("currentback");
				slides[0].classList.remove("currentback");
				slides[1].classList.remove("currentback");
			}
			else if(slideNumber == 2){
				slides[0].classList.add("currentback");
				slides[1].classList.remove("currentback");
				slides[2].classList.remove("currentback");
			}
			document.getElementById('slidercount').innerHTML = slideNumber+1+ " / 3"   ;
		}
		nextBtn.onclick = function(){
			currentSlide[0].classList.remove("current");
			if(slideNumber == (slides.length-1)){
				slideNumber = 0;
			}
			else{
				slideNumber = slideNumber+1;
			}
			slides[slideNumber].classList.add("current");
			if (slideNumber == 0) {
				slides[2].classList.add("currentback");
				slides[1].classList.remove("currentback");
				slides[0].classList.remove("currentback");
			}
			else if(slideNumber == 1){
				slides[0].classList.add("currentback");
				slides[2].classList.remove("currentback");
				slides[1].classList.remove("currentback");
			}
			else if(slideNumber == 2){
				slides[1].classList.add("currentback");
				slides[0].classList.remove("currentback");
				slides[2].classList.remove("currentback");
			}
			document.getElementById('slidercount').innerHTML = slideNumber+1+ " / 3"  ;
		}
	}
